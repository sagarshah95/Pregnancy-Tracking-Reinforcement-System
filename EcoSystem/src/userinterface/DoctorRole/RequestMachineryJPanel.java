    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        if(machineryJTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter machine name");
            return;
        }
        else{
        
        String mName = machineryJTextField.getText();
        
        if (!mName.matches("^[a-zA-Z]*$")) {
                JOptionPane.showMessageDialog(null, "Please enter the Machine Name using letters only");
                return;
            }

        MachineryWorkRequest request = new MachineryWorkRequest();
        request.setMachineName(mName);
        request.setSender(userAccount);
        request.setStatus("Sent");

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof MachineryOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null, "Request sent successfully");
        }
        
        populateMachineRequestJtable();
    }

    }//GEN-LAST:event_requestTestJButtonActionPerformed

