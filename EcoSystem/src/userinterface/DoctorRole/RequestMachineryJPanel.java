    public void populateMachineRequestJtable(){
        DefaultTableModel dtm = (DefaultTableModel) MachineRequestJTable.getModel();
        String response = null;
        
        dtm.setRowCount(0);
        for(WorkRequest machineRequest : userAccount.getWorkQueue().getWorkRequestList()){
            if(machineRequest instanceof MachineryWorkRequest){
                Object[] row = new Object[4];
                row[0] = machineRequest;
                //row[0] = ((MachineryWorkRequest) machineRequest).getMachineName();
                row[1] = machineRequest.getReceiver();
                row[2] = machineRequest.getStatus();
                
                response = ((MachineryWorkRequest) machineRequest).getResult();
                row[3] = response == null ? "Waiting" : response;
                dtm.addRow(row);
            }
        }
    }

