    public void populateRequestMedicineJTable(){
        DefaultTableModel model = (DefaultTableModel) medicineRequestJTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : pharmacyOrganization.getWorkQueue().getWorkRequestList()){
            if(request instanceof medicineWorkFlowRequest){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = request.getStatus();
            /*String result = ((medicineWorkRequest) request).getMediName();
            row[3] = result == null ? "Waiting" : result;*/
            
            model.addRow(row);
            }
        }
    }

