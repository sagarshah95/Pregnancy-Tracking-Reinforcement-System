    private void assignjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignjButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = medicineRequestJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
        
        WorkRequest request = (WorkRequest)medicineRequestJTable.getValueAt(selectedRow, 0);
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateRequestMedicineJTable();
    }//GEN-LAST:event_assignjButton2ActionPerformed

    private void processjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = medicineRequestJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select a row");
        }
        
        medicineWorkFlowRequest request = (medicineWorkFlowRequest)medicineRequestJTable.getValueAt(selectedRow, 0);
     
        request.setStatus("Processing");
        
        ProcessMedicineRequestJPanel processMedicineRequestJPanel = new ProcessMedicineRequestJPanel(userProcessContainer, request);
        userProcessContainer.add("ProcessMedicineRequestJPanel", processMedicineRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_processjButtonActionPerformed


